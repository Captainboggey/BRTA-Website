    <h3>Instruction for License</h3>
    <p>    To Apply for Learner Driver's License, the applicant has to approach the Bangladesh Road Transport Authority 
    An application form will be provided to the applicant by the concerned officer. or it can be downloaded from the following 
    The applicant has to fill in all the necessary details in the application form. (Incomplete applications are not accepted)
    Make sure that the applicant has attached all the necessary documents that are mentioned in the “Required Documents” section of this page.
    The applicant has to attach photocopies of the required documents along with the application form while submitting it.
    Submit all the documents with the completed form to the concerned officer.
    The applicant has to pay the prescribed fee as requested by the authority.
    After receiving the application, the concerned authority will check the submitted application and document set. If all are in place, the application will be accepted.
    An acknowledgment receipt will be provided. Please save it for future reference.
    Then the application will be processed further. All the updates on the application status will be sent via SMS to the applicant’s mobile number.
    Once the process gets completed, the applicant gets a call from the office regarding the application.
    The applicant has to visit the office to complete the written exam.
    After completing, the applicant has to vision test.
    Once the process gets completed, the applicant will get the license from the officer.
    This entire process can be completed within 3- 4 weeks or as per the timeline advised by the respective department authority..</p>
